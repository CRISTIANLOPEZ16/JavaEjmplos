     public void listacombo(){
    
     if(!this.mercado.getSucursal().isEmpty())
     {
     String direccion[] = new String[this.mercado.getSucursal().size()];
     for(int i=0;i<this.mercado.getSucursal().size();i++)
     {
         direccion[i] = this.mercado.getSucursal().get(i).getDireccion();
     }
     jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(direccion));
    }
     else{jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "No hay sucursales" }));}
    }
